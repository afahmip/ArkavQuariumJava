package com.arkavquarium.movable;

/**
 * TypeMove is the enumeration of the types of move that can be used by an object.
 *
 * <ol> <li>RANDOM       The object is moving randomly</li> <li>NOT_RANDOM   The object is moving
 * according to something else <li>STOP         The object is not moving</li> </ol>
 *
 * @author Adylan Roaffa
 * @version 1.1
 */
public enum TypeMove {
  RANDOM, NOT_RANDOM, STOP
}
